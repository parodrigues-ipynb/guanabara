"""
Crie um programa que escreva 'Olá, mundo!' na tela.
"""

# Solução
print("Olá, mundo!")

# Solução extra porque fiquei entediado
"""
A função print() pelo visto sempre
"""
str = "Olá, mundo!"
for letter in str:
    print(letter, end='')   # A função print() tem um argumento end='\n' por padrão

